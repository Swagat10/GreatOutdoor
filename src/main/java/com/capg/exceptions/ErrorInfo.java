package com.capg.exceptions;

public class ErrorInfo 
{

	private String alert;

	public ErrorInfo() {
		super();
	}

	public ErrorInfo(String alert) {
		super();
		this.alert = alert;
	}

	public String getAlert() {
		return alert;
	}

	public void setAlert(String alert) {
		this.alert = alert;
	}
}
